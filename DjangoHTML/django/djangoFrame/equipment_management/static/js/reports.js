        function downloadExcel() {
            const flattenedData = jsonData.reduce((acc, curr) => {
                for (const key in curr) {
                    if (Array.isArray(curr[key])) {
                        acc = acc.concat(curr[key].map(item => ({ ...item, category: key })));
                    }
                }
                return acc;
            }, []);

            const worksheet = XLSX.utils.json_to_sheet(flattenedData);
            // console.log("Worksheet:", worksheet); // Log worksheet to console

            const workbook = XLSX.utils.book_new();
            XLSX.utils.book_append_sheet(workbook, worksheet, 'Sheet1');

            XLSX.writeFile(workbook, 'inventory.xlsx');
        }
        document.getElementById('downloadButton').onclick = downloadExcel;